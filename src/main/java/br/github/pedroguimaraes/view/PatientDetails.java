    private void btnPdfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPdfActionPerformed
        Document document = new Document();
        Date date = new Date();

        //Abre janela pra escolher o diretório 
        JFileChooser fc = new JFileChooser();
        fc.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int res = fc.showOpenDialog(null);
        
        //Verifica se o arquivo escolhido é válido
        if (res == JFileChooser.APPROVE_OPTION) {
            File dir = fc.getSelectedFile();
            try {
                UUID fileName = java.util.UUID.randomUUID();
                PdfWriter.getInstance(document, new FileOutputStream(dir.getName() + "\\" + fileName + ".pdf"));
                document.open();
                
                
                /** MONTAGEM DO PDF **/
                //Adiciona um parágrafo 
                document.add(new Paragraph("Relatório de agendamento criado no dia: " + new SimpleDateFormat("dd/MM/yyyy").format(date) + "\n\n\n"));
                
                
                
                PdfPTable table = new PdfPTable(new float[] { 1.0f, 1.0f, 1.0f, 1.0f});
                table.addCell(new Phrase("Paciente"));
                table.addCell(new Phrase("Cartão sus"));
                table.addCell(new Phrase("CPF"));
                table.addCell(new Phrase("Contato"));
                
                for(Patient patient : this.scheduling.gePatients()) {
                    table.addCell(new PdfPCell(new Phrase(patient.getPerson().getName())));
                    table.addCell(new PdfPCell(new Phrase(patient.getSusCard())));
                    table.addCell(new PdfPCell(new Phrase(patient.getPerson().getCPF())));
                    table.addCell(new PdfPCell(new Phrase(patient.getPerson().getTelephone())));
                }
                
                //Adiciona tabela
                document.add(table);
                
                //Criando palavra em negrito para destacar 
                document.add(new Paragraph("\n\nExame que será feito: " + this.scheduling.getExam().getName() + 
                        "\n\nDescrição do exame: " + this.scheduling.getExam().getDescription() + "\n\n"));
                
                document.add(new Paragraph("Data de realização: " + this.scheduling.getScheduleTime()+ "\n\n"));
                document.add(new Paragraph("Data prevista de chegada: " + this.scheduling.getArrivalTime()+ "\n\n"));
                document.add(new Paragraph("Endereço: " + this.scheduling.getAddress() + ", " +
                                                          this.scheduling.getNumber()  + ", " +
                                                          this.scheduling.getNeighborhood() + ", " +
                                                          this.scheduling.getCity() + " - " +
                                                          this.scheduling.getState() + ", " +
                                                          this.scheduling.getCountry()
                ));
                
                JOptionPane.showMessageDialog(null, "Arquivo criado em: " + dir.getName() + "\\" + fileName);
            
                /** FIM DA MONTAGEM DO PDF **/
                
                
            } catch (DocumentException ex) {
                System.err.print(ex);
            } catch (IOException ex) {
                System.err.print(ex);
            }
           
            document.close();
        }else {
            JOptionPane.showMessageDialog(null, "Por favor escolha um diretório válido.");
        }
    }//GEN-LAST:event_btnPdfActionPerformed

