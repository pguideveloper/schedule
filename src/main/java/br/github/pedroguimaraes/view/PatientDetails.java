    public void setFields() {
        DefaultTableModel model = (DefaultTableModel) this.tblPatients.getModel();

        this.patients = this.scheduling.gePatients();

        model.setNumRows(0);
        for (Patient patient : this.patients) {
            model.addRow(new String[]{
                patient.getPerson().getName(),
                patient.getSusCard(),
                patient.getPerson().getCPF(),
                patient.getPerson().getTelephone() + " / " + patient.getPerson().getCelphone()
            });
        }

        this.tblPatients.setModel(model);

        this.txtExam.setText(this.scheduling.getExam().getName());
        this.txtDriver.setText(this.scheduling.getDriver().getPerson().getName());
        this.txtVehicle.setText(this.scheduling.getVehicle().getType());
        this.txtCep.setText("123");
        this.txtAddress.setText(this.scheduling.getAddress());
        this.txtNeighb.setText(this.scheduling.getNeighborhood());
        this.txtNumber.setText(this.scheduling.getNumber());
        this.txtCity.setText(this.scheduling.getCity());
        this.txtState.setText(this.scheduling.getState());
        this.txtCountry.setText(this.scheduling.getCountry());
        this.txtScheduleTime.setText(new SimpleDateFormat("dd/MM/yyyy hh:mm:ss").format(scheduling.getScheduleTime()));
        this.txtArrivalTime.setText(new SimpleDateFormat("dd/MM/yyyy hh:mm:ss").format(scheduling.getArrivalTime()));
    }

